# Leia n (int). Faça n += 3, n *= 2, n -= 4, n //= 3. Mostre cada etapa usando a função print().

n = int(input('Digite um número inteiro: '))

n += 3
print('Depois de somar 3:', n)

n *= 2
print('Depois de multiplicar por 2:', n)

n -= 4
print('Depois de subtrair 4:', n)

n //= 3
print('Depois de dividir por 3:', n)

